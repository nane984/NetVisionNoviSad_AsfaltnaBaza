    private String[] getText(List<db.Receptura> recepti){
    
        String[] nazivi = new String[40];
        
        for(int i=0; i<40; i++){
            if(i<recepti.size()){
                nazivi[i] = recepti.get(i).getReceptura();
            }else{
                nazivi[i] = "Recept "+i;
            }
       }
        return nazivi;
    }


