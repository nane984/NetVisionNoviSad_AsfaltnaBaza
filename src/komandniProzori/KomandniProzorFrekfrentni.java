    public void startScreenRefresh() {
        timer = new Timer(300, new ActionListener() {  //1000 1 sec
            @Override
            public void actionPerformed(ActionEvent e) {
                zadataFr.setValue(Convert.shiftPointDoubleSign(jSliderFrekfrencija.getValue(), frekfrentni.getFrekfrencija().getBrojDecimala()));
                ostvarenaFr.setValue(Convert.shiftPointDoubleSign(frekfrentni.getFrekfrencija().getVrednost(), frekfrentni.getFrekfrencija().getBrojDecimala()));
                
                //jSliderFrekfrencija.setEnabled(frekfrentni.getPotvrdaRada().isVrednost() && !frekfrentni.getGreska().isVrednost());
                //jToggleButtonUkljuci.setEnabled(!frekfrentni.getPotvrdaRada().isVrednost() && !frekfrentni.getGreska().isVrednost());
                //jToggleButtonIskljuci.setEnabled(frekfrentni.getPotvrdaRada().isVrednost() && !frekfrentni.getGreska().isVrednost());
                img.setImg(jLabelVentil, frekfrentni.getPotvrdaRada().isVrednost(), frekfrentni.getGreska().isVrednost(), frekfrentni.getTipSlike());
            }
        });
        jSliderFrekfrencija.setValue(frekfrentni.getFrekfrencija().getVrednost());
       
    }
    
    public void timerStart(){
         timer.start();
    }
    
    public void timerStop(){
         timer.stop();
    }

