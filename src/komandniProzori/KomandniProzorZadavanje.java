    private void initTimer() {
        timer = new Timer(300, new ActionListener() {  //1000 1 sec
            @Override
            public void actionPerformed(ActionEvent e) {
                getZalihe();
            }
        });
    }
    
    public void timerStart() {
        timer.start();
    }

    public void timerStop() {
        timer.stop();
    }
    
    public void getZalihe(){
        jLabelOpis.setText(zadato.getOpis());
        StanjeZalihe.setText(Convert.shiftPointUnsign(zadato.getZadata().getVrednost(),zadato.getBrDec()));
    }
    
    public void setStanjeZaliha(){
        
        int result = Integer.parseInt(zalihe.getValue().toString());
        
        NetVision.mb.writeMW(result, zadato.getZadata().getAdresaVrednosti());
        
        zalihe.setValue("0");

    }

