    public void startScreenRefresh() {
        timer = new Timer(300, new ActionListener() {  //1000 1 sec
            @Override
            public void actionPerformed(ActionEvent e) {
                    if(img.getKlapne() == tipSlike){
                    //jToggleButtonOtvori.setEnabled(!potvrdaRada.isVrednost() && !greska.isVrednost());
                    //jToggleButtonZatvori.setEnabled(potvrdaRada.isVrednost() && !greska.isVrednost());
                    jToggleButtonOtvori.setText("Otvori");
                    jToggleButtonZatvori.setText("Zatvori");
                    ledOtvoren.setLedOn(otvoren.isVrednost());
                    ledZatvoren.setLedOn(zatvoren.isVrednost());
                    img.setImg(jLabelVentil, potvrdaRada.isVrednost(), greska.isVrednost(), tipSlike);
                    }
                    else{
                         ledOtvoren.setLedOn(otvoren.isVrednost());
                        ledZatvoren.setLedOn(zatvoren.isVrednost());
                        jToggleButtonOtvori.setText("Ukljuci");
                        jToggleButtonZatvori.setText("Iskljuci");
                        img.setImg(jLabelVentil, potvrdaRada.isVrednost(), greska.isVrednost(), tipSlike);
                    }
               
            }
        });
    }
    
    public void timerStart(){
         timer.start();
    }
    
    public void timerStop(){
         timer.stop();
    }

